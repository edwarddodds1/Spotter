/** 6×3 grid: rows 1–2 have six breeds; row 3 has three (Irish Wolfhound, Chow, Xolo). */
async function emitRareLegendary() {
  const ids = [
    "bernese-mountain-dog",
    "rhodesian-ridgeback",
    "weimaraner",
    "dobermann",
    "great-dane",
    "german-shorthaired-pointer",
    "samoyed",
    "pembroke-welsh-corgi",
    "greyhound",
    "basset-hound",
    "vizsla",
    "cane-corso",
    "irish-wolfhound",
    "chow-chow",
    "xoloitzcuintli",
  ];
  const imgPath = path.join(root, "assets", "rare-legendary-dogs.png");
  if (!fs.existsSync(imgPath)) {
    console.warn(`Skip rare/legendary crops: missing ${imgPath}`);
    return;
  }
  const { W, H, crops } = analyzeGrid(await Jimp.read(imgPath), 6, 3, 15);

  let s = `/**
 * Per-breed crops on assets/rare-legendary-dogs.png — regenerated by scripts/sync-sprite-crops.mjs
 * Grid: 6 columns × 3 rows (last row: three breeds). Run make-rare-legendary-transparent first if needed.
 */
export const RARE_LEGENDARY_SPRITE_SIZE = { w: ${W}, h: ${H} } as const;

export const rareLegendaryBreedCrops: Record<
  string,
  { x: number; y: number; w: number; h: number; feetY: number; dx?: number; dy?: number; scale?: number }
> = {
`;
  ids.forEach((id, i) => {
    const c = crops[i];
    s += `  ${fmtKey(id)}: { x: ${c.x}, y: ${c.y}, w: ${c.w}, h: ${c.h}, feetY: ${c.feetY} },\n`;
  });
  s += "};\n";

  const outPath = path.join(root, "src", "constants", "rareLegendarySpriteCrops.ts");
  fs.writeFileSync(outPath, s, "utf8");
  console.log(`Wrote ${outPath}`);
}

async function main() {
  const commonPath = path.join(root, "src", "constants", "commonSpriteCrops.ts");
  const uncommonPath = path.join(root, "src", "constants", "uncommonSpriteCrops.ts");
  fs.writeFileSync(commonPath, await emitCommon(), "utf8");
  fs.writeFileSync(uncommonPath, await emitUncommon(), "utf8");
  console.log(`Wrote ${commonPath}`);
  console.log(`Wrote ${uncommonPath}`);
  await emitRareLegendary();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
