/**
 * Scans common + uncommon sprite sheets: tight dog bbox (excludes label band),
 * sets feetY to bottom of ink + buffer. Writes src/constants/*SpriteCrops.ts
 * Run: node scripts/sync-sprite-crops.mjs
 * After editing sheets, run: npm run sprites:transparent
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { Jimp, intToRGBA } from "jimp";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

const DOG_ONLY_FRACTION = 0.62;
const PAD = 8;
const FEET_PAD = 2;

function isInk(r, g, b, a) {
  if (a < 40) return false;
  return r + g + b < 715;
}

function analyzeGrid(img, cols, rows, count) {
  const W = img.bitmap.width;
  const H = img.bitmap.height;
  const cellW = W / cols;
  const cellH = H / rows;
  const out = [];

  for (let i = 0; i < count; i++) {
    const col = i % cols;
    const row = Math.floor(i / cols);
    const x0 = Math.round(col * cellW);
    const y0 = Math.round(row * cellH);
    const x1 = Math.round((col + 1) * cellW);
    const yLimit = Math.round(y0 + cellH * DOG_ONLY_FRACTION);

    let minX = x1,
      minY = yLimit,
      maxX = x0,
      maxY = y0;

    for (let y = y0; y < yLimit; y++) {
      for (let x = x0; x < x1; x++) {
        const c = intToRGBA(img.getPixelColor(x, y));
        if (isInk(c.r, c.g, c.b, c.a)) {
          if (x < minX) minX = x;
          if (x > maxX) maxX = x;
          if (y < minY) minY = y;
          if (y > maxY) maxY = y;
        }
      }
    }

    if (minX > maxX) {
      const cw = Math.round(cellW * 0.85);
      const ch = Math.round(cellH * 0.45);
      minX = Math.round(x0 + (cellW - cw) / 2);
      minY = Math.round(y0 + cellH * 0.08);
      maxX = minX + cw - 1;
      maxY = minY + ch - 1;
    }

    const rawMaxY = maxY;

    minX = Math.max(x0, minX - PAD);
    minY = Math.max(y0, minY - PAD);
    maxX = Math.min(x1 - 1, maxX + PAD);
    maxY = Math.min(yLimit - 1, maxY + PAD);

    const w = maxX - minX + 1;
    const h = maxY - minY + 1;
    const feetY = Math.min(yLimit - 1, rawMaxY + FEET_PAD);
    out.push({ x: minX, y: minY, w, h, feetY });
  }
  return { W, H, crops: out };
}

function fmtKey(id) {
  return id.includes("-") ? `"${id}"` : id;
}

async function emitCommon() {
  const ids = [
    "cavoodle",
    "labrador-retriever",
    "golden-retriever",
    "french-bulldog",
    "german-shepherd",
    "border-collie",
    "staffordshire-bull-terrier",
    "groodle",
    "miniature-dachshund",
    "cavalier-king-charles-spaniel",
    "labradoodle",
    "poodle-miniature",
    "maltese",
    "jack-russell-terrier",
    "shih-tzu",
  ];
  const img = path.join(root, "assets", "common-dogs.png");
  const { W, H, crops } = analyzeGrid(await Jimp.read(img), 4, 4, 15);

  let s = `/**
 * Per-breed crops on assets/common-dogs.png — regenerated by scripts/sync-sprite-crops.mjs
 */
export const COMMON_SPRITE_SIZE = { w: ${W}, h: ${H} } as const;

export const commonBreedCrops: Record<
  string,
  { x: number; y: number; w: number; h: number; feetY: number; dx?: number; dy?: number; scale?: number }
> = {
`;
  ids.forEach((id, i) => {
    const c = crops[i];
    s += `  ${fmtKey(id)}: { x: ${c.x}, y: ${c.y}, w: ${c.w}, h: ${c.h}, feetY: ${c.feetY} },\n`;
  });
  s += "};\n";
  return s;
}

